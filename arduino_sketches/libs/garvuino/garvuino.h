int LedPin = 8;

void garvuino_BlinkLed(byte num)         // Basic blink function
{
    for (byte i=0;i<num;i++)
    {
        digitalWrite(LedPin,HIGH);
        delay(200);
        digitalWrite(LedPin,LOW);
        delay(200);
    }
}


